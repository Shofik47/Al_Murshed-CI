				<div class="section-title text-center">
				<h2>Hadith of Intention</h2>
				</div>
				<p>Umar Ibn Al-Khattab relates that he heard the Messenger of God صلى الله عليه و سلم  say : “Actions are but by intention and every man shall have but that which he intended. Thus he whose migration was for Allah and His messenger, his migration was for Allah and His messenger, and he whose migration was to achieve some worldly benefit or to take some woman in marriage, his migration was for that for which he migrated.” ~ Related by Bukhari and Muslim
				</p>
				<p>This is an extremely comprehensive and rich Hadith of the Prophet صلى الله عليه و سلم that reflects the core of the Islamic Faith.
				</p>
				<p>Imam Bukhari selected the first hadith, ‘Innamal a’malu binniyat’ of Sahih Bukhari as an introduction to the whole collection to show the sincerity of purpose of the author. It reminds the reader that good and noble deeds inculcated by the Hadith found therein will only bear fruit if there is sincerity attached to their intentions and would be meaningless if the motive is not sincere.</p>
